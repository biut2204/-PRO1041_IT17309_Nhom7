    private void LoadTaoPhong(List<Phong> list) {
        dtm = (DefaultTableModel) tblPhong.getModel();
        dtm.setRowCount(0);
        for (Phong phong : list) {
            dtm.addRow(new Object[]{
                phong.getMa(), phong.getTenPhong(),
                phong.getDienTich(), phong.getTinhTrang(), phong.getAnhPhong()});
        }
    }

