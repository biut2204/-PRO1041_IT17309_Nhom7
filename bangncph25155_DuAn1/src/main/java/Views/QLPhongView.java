    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Phong phong = new Phong();

        if (txtMa.getText().length() == 0 || txtDienTich.getText().length() == 0 || txtTenPhong.getText().length() == 0 || txtTinhTrang.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong thong tin");
        } else {
            int index = tblPhong.getRowCount() + 1;
            String ma = txtMa.getText().trim();
            String tenphong = txtTenPhong.getText().trim();
            Float dientich = Float.valueOf(txtDienTich.getText());
            String tinhtrang = txtTinhTrang.getText().trim();
            phong.setMa(ma);
            phong.setTenPhong(tenphong);
            phong.setDienTich(dientich);
            phong.setTinhTrang(tinhtrang);
            phong.setAnhPhong(hinh);
            pR.save(phong);
            JOptionPane.showMessageDialog(this, "thanh cong");
            LoadTaoPhong(pR.getAllData());
        }

    }//GEN-LAST:event_btnAddActionPerformed
    public void fillPhong(Phong p) {
        txtMa.setText(p.getMa());
        txtTenPhong.setText(p.getTenPhong());
        txtDienTich.setText(String.valueOf(p.getDienTich()));
        txtTinhTrang.setText(p.getTinhTrang());
        ImageIcon imgIcon = new ImageIcon("C:/Users/acer/OneDrive/Documents/anh/" + p.getAnhPhong());
        Image img = imgIcon.getImage();
        jlbAnh.setIcon(new ImageIcon(img.getScaledInstance(jlbAnh.getWidth(), jlbAnh.getHeight(), 0)));
    }

    public Phong show(String maPhong) {
        for (Phong p : pR.getAllData()) {
            if (p.getMa().equalsIgnoreCase(maPhong)) {
                return p;
            }
        }
        return null;
    }
    private void tblPhongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPhongMouseClicked
        // TODO add your handling code here:
        int id = tblPhong.rowAtPoint(evt.getPoint());
        String maphong = tblPhong.getValueAt(id, 0).toString();
        Phong p = show(maphong);
        fillPhong(p);
    }//GEN-LAST:event_tblPhongMouseClicked

